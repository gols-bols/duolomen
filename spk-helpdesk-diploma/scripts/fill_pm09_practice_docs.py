def set_run_font(run, size=14, bold=False) -> None:
    run.font.name = "Times New Roman"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    run.font.size = Pt(size)
    run.bold = bold


def normalize_paragraph(paragraph, *, align=WD_ALIGN_PARAGRAPH.JUSTIFY, first_indent=True) -> None:
    fmt = paragraph.paragraph_format
    fmt.line_spacing = 1.5
    fmt.first_line_indent = Cm(1.25) if first_indent else Cm(0)
    fmt.space_after = Pt(0)
    paragraph.alignment = align
    for run in paragraph.runs:
        if run.text:
            set_run_font(run, 14, run.bold)


def set_paragraph_text(paragraph, text: str, *, bold=False, center=False) -> None:
    paragraph.clear()
    run = paragraph.add_run(text)
    set_run_font(run, 14 if not bold else 16, bold)
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.JUSTIFY
    fmt = paragraph.paragraph_format
    fmt.line_spacing = 1.5
    fmt.first_line_indent = Cm(0 if center else 1.25)
    fmt.space_after = Pt(0)


def set_cell_text(cell, text: str) -> None:
    cell.text = text
    for p in cell.paragraphs:
        normalize_paragraph(p, first_indent=False)


def replace_text_everywhere(doc: Document, old: str, new: str) -> None:
    for paragraph in doc.paragraphs:
        if old in paragraph.text:
            set_paragraph_text(
                paragraph,
                paragraph.text.replace(old, new),
                center=paragraph.alignment == WD_ALIGN_PARAGRAPH.CENTER,
            )
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                if old in cell.text:
                    set_cell_text(cell, cell.text.replace(old, new))


def replace_in_paragraph(paragraph, replacements: dict[str, str]) -> None:
    text = paragraph.text
    new_text = text
    for old, new in replacements.items():
        new_text = new_text.replace(old, new)
    if new_text != text:
        paragraph.clear()
        run = paragraph.add_run(new_text)
        set_run_font(run, 14)
        normalize_paragraph(paragraph)


